public class HalfPyramidNumbers {
    
        public static void main(String[] args) {
            int r=5;
            int i,j;
            for(i=1;i<=r;i++)
            {
                for(j=1;j<=i;j++)
                {
                    System.out.print(+j);
                }
                System.out.println();
            }
        }
    
    
}
