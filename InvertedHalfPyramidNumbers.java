public class InvertedHalfPyramidNumbers {
  
        public static void main(String[] args) {
            int r=4;
            int i,j;
            for(i=1;i<=r;i++)
            {
                for(j=r;j>=1;j--)
                {
                    System.out.print("");
                }
                System.out.println();
            }
        }
    
}
